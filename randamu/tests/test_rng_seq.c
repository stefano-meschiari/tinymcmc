#include <assert.h>
#include "../src/rng.c"

int main() {
  // Test the sequence agains the original author's code:
  // http://www.iro.umontreal.ca/~panneton/well/WELL1024a.c
  // http://www.iro.umontreal.ca/~panneton/well/WELL1024a.h
  double original_42[8] = {
    0.36639491328969597816467285156250,
    0.50952346064150333404541015625000,
    0.64088401081971824169158935546875,
    0.84615578968077898025512695312500,
    0.51478143618442118167877197265625,
    0.84896292909979820251464843750000,
    0.93675231607630848884582519531250,
    0.27560931025072932243347167968750
  };
  double original_47[8] = {
    0.05416530068032443523406982421875,
    0.38798197219148278236389160156250,
    0.81206099549308419227600097656250,
    0.62592891883105039596557617187500,
    0.74837345513515174388885498046875,
    0.19666829798370599746704101562500,
    0.87435035570524632930755615234375,
    0.48180615971796214580535888671875
  };
  rd_rng r42, r47;
  rd_rng_init(&r42, 42);
  rd_rng_init(&r47, 47);
  int i = 0;
  for (; i < 8; ++i) {
    assert(original_42[i] == rd_rng_double(&r42));
    assert(original_47[i] == rd_rng_double(&r47));
  }
  return EXIT_SUCCESS;
}

